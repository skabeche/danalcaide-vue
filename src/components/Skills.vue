<script setup>
  import { onMounted, useTemplateRef } from "vue";

  import { useSkills } from "@/data/skills";

  import { gsap } from "gsap"
  import { ScrollTrigger } from "gsap/ScrollTrigger"
  import { SplitText } from "gsap/SplitText";

  gsap.registerPlugin(ScrollTrigger, SplitText);

  const skills = useSkills()
  const skillsRef = useTemplateRef('skillsRef')
  const svgRowsRef = useTemplateRef('svgRowsRef')
  const skillsContentRef = useTemplateRef('skillsContentRef')

  onMounted(async () => {
    gsap.set(skillsContentRef.value, { autoAlpha: 0 });

    const paths = svgRowsRef.value.querySelectorAll('path')
    // Gets element from projects component. Pretty weak, it breaks Vue principles but it works for now.
    const projectsInfo = document.querySelector('.projects-info');

    paths.forEach(path => {
      const length = path.getTotalLength()
      gsap.set(path, {
        strokeDasharray: length,
        strokeDashoffset: -length
      })
    })

    gsap.set(paths, { autoAlpha: 0 });

    const tl = gsap.timeline({
      defaults: {
        ease: 'none',
      },
      scrollTrigger: {
        trigger: skillsRef.value,
        start: "top top",
        end: "+=500%",
        pin: true,
        scrub: 1.2,
        toggleActions: "play none none reverse",
        // markers: true,
      },
    });

    tl
      .to(paths, {
        // Add a slight delay before starting the animation to allow the user to see the previous animation a bit more.
        delay: window.matchMedia('(min-width: 768px)').matches ? .8 : 0,
        strokeDashoffset: 0,
        stagger: {
          each: 0.06,
          from: "start"
        },
        onStart: () => {
          gsap.set(projectsInfo, { zIndex: 0 });
          gsap.set(paths, { autoAlpha: 1 });
        },
        onComplete: () => {
          gsap.set(skillsContentRef.value, { autoAlpha: 1 });
        },
        onReverseComplete: () => {
          gsap.set(projectsInfo, { zIndex: 20 });
        }
      })
      .to(paths, {
        strokeDashoffset: (i, el) => el.getTotalLength(),
        stagger: {
          each: 0.05,
          from: "random"
        },
        onReverseComplete: () => {
          gsap.set(skillsContentRef.value, { autoAlpha: 0 });
        }
      });
  });
</script>
